class Product
{
    constructor(id, productType, title, mark, model, manufacturer, duration, group, description, material, age, language, gender, dimension, weight)
    {
        this.id = id;
        this.productType = productType;
        this.title = title;
        this.mark = mark;
        this.model = model;
        this.manufacturer = manufacturer;
        this.duration = duration;
        this.group = group;
        this.description = description;
        this.material = material;
        this.age = age;
        this.language = language;
        this.gender = gender;
        this.dimension = dimension;
        this.weight = weight;
    }

    
}